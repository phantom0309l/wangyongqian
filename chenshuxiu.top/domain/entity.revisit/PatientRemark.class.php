    public static function createByBiz ($row) {
        DBC::requireNotEmpty($row, "PatientRemark::createByBiz row cannot empty");

        if ($row["patientid"] == null) {
            $row["patientid"] = 0;
        }

        if ($row["doctorid"] == null) {
            $row["doctorid"] = 0;
        }

        $default = array();
        $default["wxuserid"] = 0;
        $default["userid"] = 0;
        $default["patientid"] = 0;
        $default["doctorid"] = 0;
        $default["revisitrecordid"] = 0;
        $default["thedate"] = date('Y-m-d');
        $default["typestr"] = '';
        $default["name"] = '';
        $default["content"] = '';

        $row += $default;
        return new self($row);
    }

