            'doctorid',  // doctorid
            'revisitrecordid',  // revisitrecordid
            'thedate',  // 记录的日期
            'typestr',  // 类型
            'name',  // 标题
            'content'); // 具体内容

    }

    protected function init_keys_lock () {
        $this->_keys_lock = array(
            'wxuserid',
