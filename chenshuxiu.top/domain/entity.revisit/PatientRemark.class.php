            "key" => "userid");
        $this->_belongtos["patient"] = array(
            "type" => "Patient",
            "key" => "patientid");
        $this->_belongtos["doctor"] = array(
            "type" => "Doctor",
            "key" => "doctorid");
        $this->_belongtos["revisitrecord"] = array(
            "type" => "RevisitRecord",
            "key" => "revisitrecordid");
    }

