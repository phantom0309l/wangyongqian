    public static function createByBiz ($row) {
        DBC::requireNotEmpty($row, "Recipe::createByBiz row cannot empty");

        if ($row["patientid"] == null) {
            $row["patientid"] = 0;
        }

        if ($row["doctorid"] == null) {
            $row["doctorid"] = 0;
        }

        $default = array();
        $default["wxuserid"] = 0;
        $default["userid"] = 0;
        $default["patientid"] = 0;
        $default["doctorid"] = 0;
        $default["pictureid"] = 0;
        $default["thedate"] = '0000-00-00';
        $default["status"] = 0;
        $default["remark"] = '';

        $row += $default;
        return new self($row);
    }

