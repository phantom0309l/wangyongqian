    public static function createByBiz ($row) {
        DBC::requireNotEmpty($row, "Like::createByBiz row cannot empty");

        if ($row["wxuserid"] == null) {
            $row["wxuserid"] = 0;
        }

        if ($row["userid"] == null) {
            $row["userid"] = 0;
        }

        if ($row["patientid"] == null) {
            $row["patientid"] = 0;
        }

        $default = array();
        $default["wxuserid"] = 0;
        $default["userid"] = 0;
        $default["patientid"] = 0;
        $default["objtype"] = '';
        $default["objid"] = 0;
        $default["status"] = 0;

        $row += $default;
        return new self($row);
    }

